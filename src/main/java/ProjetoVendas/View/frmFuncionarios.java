    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String nome = txtPesquisa.getText() + "%";

        FuncionariosDao dao = new FuncionariosDao();
        List<Funcionarios> lista = dao.buscarFuncionarioPorNome(nome);

        DefaultTableModel dados = (DefaultTableModel) tblFuncionarios.getModel();
        dados.setNumRows(0);

        for (Funcionarios c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getRg(),
                c.getCpf(),
                c.getEmail(),
                c.getSenha(),
                c.getCargo(),
                c.getNivel_acesso(),
                c.getTelefone(),
                c.getCelular(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getUf()
            });
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnPesquisarDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarDadosActionPerformed

        String nome = txtNome.getText();
        Funcionarios obj = new Funcionarios();
        FuncionariosDao dao = new FuncionariosDao();

        obj = dao.consultaPorNome(nome);

        if (obj.getNome() != null) {

            txtCodigo.setText(String.valueOf(obj.getId()));
            txtNome.setText(obj.getNome());
            ftxtRG.setText(obj.getRg());
            ftxtCPF.setText(obj.getCpf());
            txtEmail.setText(obj.getEmail());
            txtSenha.setText(obj.getSenha());
            txtCargo.setText(obj.getCargo());
            cbxNivelAcesso.setSelectedItem(obj.getNivel_acesso());
            ftxtTelefone.setText(obj.getTelefone());
            ftxtCelular.setText(obj.getCelular());
            ftxtCep.setText(obj.getCep());
            txtEndereco.setText(obj.getEndereco());
            txtNumero.setText(String.valueOf(obj.getNumero()));
            txtComp.setText(obj.getComplemento());
            txtBairro.setText(obj.getBairro());
            txtCidade.setText(obj.getCidade());
            cbxUF.setSelectedItem(obj.getUf());

        } else {
            JOptionPane.showMessageDialog(null, "Funcionário não encontrado.");
        }
    }//GEN-LAST:event_btnPesquisarDadosActionPerformed

